export async function streamFile(messageId, res, range) {
    await initClient();
    
    // 1. Obtener info del archivo
    const msgs = await client.getMessages(chatId, { ids: [Number(messageId)] });
    if (!msgs || !msgs[0]) throw new Error("Mensaje no encontrado");
    const msg = msgs[0];

    let location = null;
    let fileSize = 0;
    let mimeType = "application/octet-stream";

    if (msg.media) {
        if (msg.media.document) {
            const doc = msg.media.document;
            fileSize = Number(doc.size);
            mimeType = doc.mimeType;
            location = new Api.InputDocumentFileLocation({
                id: doc.id,
                accessHash: doc.accessHash,
                fileReference: doc.fileReference,
                thumbSize: ""
            });
        } else if (msg.media.photo) {
            const photo = msg.media.photo;
            const size = photo.sizes[photo.sizes.length - 1];
            fileSize = size.size;
            mimeType = "image/jpeg";
            location = new Api.InputPhotoFileLocation({
                id: photo.id,
                accessHash: photo.accessHash,
                fileReference: photo.fileReference,
                thumbSize: size.type
            });
        }
    }

    if (!location) throw new Error("Sin archivo v√°lido");

    // 2. Preparar Rangos
    // Si hay rango, lo usamos. Si no, descargamos todo.
    let start = 0;
    let end = fileSize - 1;

    if (range) {
        const parts = range.replace(/bytes=/, "").split("-");
        start = parseInt(parts[0], 10);
        end = parts[1] ? parseInt(parts[1], 10) : fileSize - 1;
    }

    const chunksize = (end - start) + 1;
    console.log(`üé¨ Stream: ${start}-${end} (${chunksize} bytes)`);

    // 3. Escribir Cabeceras HTTP
    if (range) {
        res.writeHead(206, {
            'Content-Range': `bytes ${start}-${end}/${fileSize}`,
            'Accept-Ranges': 'bytes',
            'Content-Length': chunksize,
            'Content-Type': mimeType,
        });
    } else {
        res.writeHead(200, {
            'Content-Length': fileSize,
            'Content-Type': mimeType,
        });
    }

    // 4. BUCLE DE DESCARGA SEGURO
    // Usamos 128KB. Es el tama√±o "m√°gico" de Telegram que nunca falla.
    const KB_BLOCK = 128 * 1024; 
    let currentOffset = BigInt(start);
    const finalByte = BigInt(end);

    try {
        while (currentOffset <= finalByte) {
            // TRUCO IMPORTANTE:
            // Telegram exige que 'limit' sea m√∫ltiplo de 4KB.
            // NO calculamos cu√°nto falta. Pedimos SIEMPRE 128KB.
            // Si el archivo se acaba antes, Telegram nos devolver√° menos bytes y el bucle terminar√° solo.
            
            const result = await client.invoke(new Api.upload.GetFile({
                location: location,
                offset: currentOffset,
                limit: KB_BLOCK // <--- ESTO ES CONSTANTE. NUNCA CAMBIA.
            }));

            if (!result || result.bytes.length === 0) break;

            // Escribimos al navegador
            res.write(result.bytes);
            
            // Avanzamos
            currentOffset += BigInt(result.bytes.length);

            // Si Telegram nos devolvi√≥ menos de lo que pedimos, significa que se acab√≥ el archivo.
            if (result.bytes.length < KB_BLOCK) break;

            // Si el usuario cerr√≥ el video, paramos para ahorrar RAM
            if (res.writableEnded || res.closed) break;
        }
    } catch (err) {
        console.error("‚ùå Error en Stream:", err);
    } finally {
        if (!res.writableEnded) res.end();
    }
}

